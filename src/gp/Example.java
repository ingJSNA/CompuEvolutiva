package gp;

public class Example {

	private int answer;
	private int x;
	private int y;

	/**
	 * @param answer
	 * @param x
	 * @param y
	 */
	public Example(int answer, int x, int y) {
		super();
		this.answer = answer;
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the answer
	 */
	public int getAnswer() {
		return answer;
	}

	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

}
